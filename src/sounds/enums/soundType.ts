/**
 * An interface of Sounds the server is able to send to players.
 * 
 * WARNING: At any time, sounds may be added/removed from Minecraft!
 * There is no guarantee the sounds will play.
 * 
 * @see https://hub.spigotmc.org/javadocs/bukkit/org/bukkit/Sound.html
 */

// ============================================
// INTERFACE
// ============================================

export interface BukkitSound {
  /**
   * Return the namespaced identifier for this sound.
   * @returns This sound's key
   */
  getKey(): any;

  /**
   * Returns the name of this sound.
   * @returns The name
   */
  name(): string;

  /**
   * Returns the string representation.
   * @returns String representation
   */
  toString(): string;
}

